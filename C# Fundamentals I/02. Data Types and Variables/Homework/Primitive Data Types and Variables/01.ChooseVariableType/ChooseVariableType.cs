using System;

class ChooseVariableType
{
    static void Main()
    {
        ushort myUInt16 = 52130;
        sbyte mySByte = -115;
        uint myUInt32 = 4825932;
        byte myByte = 97;
        short myInt16 = -10000;

        Console.WriteLine("myUInt16 (ushort) = {0}", myUInt16);
        Console.WriteLine("mySByte (sbyte) = {0}", mySByte);
        Console.WriteLine("myUInt32 (uint) = {0}", myUInt32);
        Console.WriteLine("myByte (byte) = {0}", myByte);
        Console.WriteLine("myInt16 (short) = {0}", myInt16);

  
    }
}
