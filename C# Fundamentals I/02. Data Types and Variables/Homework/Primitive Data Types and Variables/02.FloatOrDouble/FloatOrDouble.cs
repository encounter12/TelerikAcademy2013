using System;

class FloatOrDouble
{
    static void Main()
    {
        double myFirstDouble = 34.567839023;
        float myFirstFloat = 12.345f;
        double mySecondDouble = 89234345.1234857;
        float mySecondFloat = 3456.091f;

        Console.WriteLine("myFirstDouble = {0}", myFirstDouble);
        Console.WriteLine("myFirstFloat = {0}", myFirstFloat);
        Console.WriteLine("mySecondDouble = {0}", mySecondDouble);
        Console.WriteLine("mySecondFloat = {0}", mySecondFloat);

    }
}

